-----------------------   РАБОТА С ТЕКСТОМ (СПИСКОМ)   ----------------------

1) Для евеличения текста используется класс lead

2) Выравнивание текста базисными классами
 class="lead text-justify" class="text-right" lass="text-left"

3) можно менять регистр символов базисными классами
    class="text-uppercase"    class="text-lowercase"

4) Для цитат выравнять текст направо class="blockquote-reverse"

5) При работе со списком для выстраивания в ряд необхлодимо применять
            <ul class="list-inline">
                <li>Пункт 1 меню</li>
            </ul>

6) При работе со спискам определений для выстраивания список типа парм-знач
            <dl class="dl-horizontal">
                <dt>Пункт термина 1</dt>
                <dd>Определение термина 1</dd>
            </dl>

