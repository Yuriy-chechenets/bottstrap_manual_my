----------------------------   РАБОТА С ТАБЛИЦА   ---------------------------

1) Для любой таблицы подключить class="table"

2) Для оформления зебры (цветового разделения обычного) class="table=striped"
        <table class="table table-striped">

3) Для оформлению бордюров класс table-bordered

4) table-hover - придает выделение строкам при наведении

5) table-condensed  -  придает более странный вид (типо компактный)

6)  Для цветового оформления строк таблицы используются классы для строк

    .active 	Изменяет цвет строки или ячейки при наведении на нее курсора мыши
    .success 	Показывает успешное или позитивное действие
    .info 	    Показывает нейтральное информативное изменение или действие
    .warning 	Показывает предупреждения, которым нужно уделить внимание
    .danger 	Показывает опасное или потенциально негативное действие

7) <div class="table-responsive">  -  оформляет блок для таблицы,
когда таблица оформлена за пределами блока контейнера, придавая ей св-ва резиновости.

